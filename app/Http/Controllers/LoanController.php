     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'amount_words' => 'required',
            'amount_figures' =>'required',
            'loan_amount'=>'required',
            'interest_rate'=>'required',
            'interest_amount' =>'required',
            'total'=>'required',
            'loan_period'=>'required',
            'disbursement_mode'=>'required',
            'loan_product' =>'required'
            ]);
        $applicant = new Loan();
        $applicant->applicant_id =Auth::user()->id;
        $applicant->fill($validatedData);
        $applicant->save();
        return redirect('/myloans');

    }

